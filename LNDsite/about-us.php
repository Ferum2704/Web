   <h3><strong>Early history</strong></h3> 
<p> The first few seasons ain't known but it was said that they had a final group of four between 1910-11 up to 1915-16 were an expansion of the league to six teams which played over ten rounds. Throughout the early 1910s, Racing Club Luxembourg and Sporting Club Luxembourg won the title on three occasions before US Hollerich became the first Luxembourg team to complete an unbeaten season winning the 1916–17 title by nine points over second-place CS Fola Esch.
</p>
<p> The start of new decade saw Stade Dudelange, Jeunesse Esch and CS Fola Esch (after coming second three times) each claiming their first Luxembourg titles. The competition though was kept very tight during that time as Stade Dudelange eventually broke through the different winners cycle to take out the 1923 title over FA Red Boys Differdange. In 1925, CA Spora Luxembourg took out the title by one point over Stade Dudelange in the final match of that particular season. The following season they would defend their title before finishing fifth in the 1927 season while Union Luxembourg claimed their first title. CS Fola Esch and CA Spora Luxembourg would win consecutive titles in 1928 and 1929 with Spora Luxembourg winning 1933 and 1934. The last two seasons before the war would be won by Stade Dudelange as they won by 4 and 7 points respectively.
</p>
<br>
<h3> <strong>After the war</strong> </h3>
<p> After the second World War cause the competition to not exist for four years, the Luxembourg National Division came back in 1944 with a six team knockout competition. Though it was the same result with Stade Dudelange getting back to what they did best. Taking out titles, as they would win the first four titles after the war. It wouldn't be until 1949 where a new champion was crowned with Sporta Luxemcourg taking out their sixth title. During the time of different champions though, National Schifflange couldn't break through as they finished runner-up in three straight seasons before falling off their chance of getting a title. The following year in 1953, FC Progrès Niedercorn secured their first title in twenty years before finishing second in 1955 and 1956. They would finish ahead of Jeunesse Esch who would win the following year.
</p>
